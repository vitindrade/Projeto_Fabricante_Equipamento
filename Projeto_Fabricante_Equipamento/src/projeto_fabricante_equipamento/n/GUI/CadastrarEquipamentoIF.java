    private void cadastrar_btActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrar_btActionPerformed
        if(nome_text.getText().isEmpty() || preco_text.getText().isEmpty() || fabricante_combo.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(rootPane, "Por favor, preencha os todos os campos!");
            return;
        } else{
            Fabricante fabricante = (Fabricante) fabricante_combo.getSelectedItem();
            Equipamento equipamento = new Equipamento(fabricante.getIdFabricante(),nome_text.getText(), Float.parseFloat(preco_text.getText()));
            EquipamentoDB equipamentoDB = new EquipamentoDB();
            boolean res = equipamentoDB.insert(equipamento);
            if(res){
                JOptionPane.showMessageDialog(this,"Equipamento cadastrado com sucesso!");
                nome_text.setText("");
                preco_text.setText("");
                fabricante_combo.setSelectedIndex(-1);
            }
            
        }
        
    }//GEN-LAST:event_cadastrar_btActionPerformed


